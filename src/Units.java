public class Units {

    String CRISID;

    String en_name;

    public Units(String CRISID, String en_name) {
        this.CRISID = CRISID;
        this.en_name = en_name;
    }

    public String getCRISID() {
        return CRISID;
    }

    public String getEn_name() {
        return en_name;
    }



}
